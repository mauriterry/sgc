    public void crearLista(){
        if (validarLista()) {
            int id = Integer.parseInt(jTextFieldID.getText());
            if (!principalFrame.existeLista(id)) {
                String nombre = jTextFieldNombreLista.getText();
                if (principalFrame.crearLista(id, nombre)) {
                    limpiar();
                    cargarCancionesDelSistema();
                    JOptionPane.showMessageDialog(this, "La lista se creo", "Crear Lista", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "La lista no se pudo crear", "Crear Lista", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "La Lista ya existe", "Crear Lista", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Faltan Datos necesarios para crear la lista", "Crear Lista", JOptionPane.WARNING_MESSAGE);
        }
                
    }
    
